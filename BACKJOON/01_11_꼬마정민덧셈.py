"""
✅문제 설명
문제
꼬마 정민이는 이제 A + B 정도는 쉽게 계산할 수 있다. 이제 A + B + C를 계산할 차례이다!

입력
첫 번째 줄에 A, B, C (1 ≤ A, B, C ≤ 1012)이 공백을 사이에 두고 주어진다.

출력
A+B+C의 값을 출력한다.

✅목적: 공백 제거 후 덧셈 확인

"""

# 코드 작성

# A, B, C 입력 받기
A, B, C = map(int, input().split(' '))

# A+B+C 계산 후 출력
print(A+B+C)




# split()에 인자를 지정하지 않아도 
# 기본적으로 공백을 기준으로 나누므로 둘의 동작은 동일




