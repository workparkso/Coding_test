"""
✅문제 설명
주어진 코드는 변수에 데이터를 저장하고 출력하는 코드입니다.
아래와 같이 출력되도록 빈칸을 채워 코드를 완성해 주세요.

출력 예시:
3
2
1
Let's go!

✅목적: \n의 사용 방법을 이해하는지 확인
"""

# 빈칸을 채워 아래 코드 완성
message = "Let's go!"

print("3\n2\n1")  # 숫자 사이 줄바꿈 추가
print(message)


"""
✅추가적인 지식

Escape Sequence (이스케이프 문자)
--------------------------------------------------
\\n    → 출력에서 줄바꿈
\\t    → 출력에서 tab 간격으로 띄어쓰기
\\\\    → print 시 출력에 백슬래시(\\) 출력
\\'    → print 시 출력에 작은따옴표(') 출력
\\"    → print 시 출력에 큰따옴표(") 출력

언제 어떤 방식을 사용해야 할까?
--------------------------------------------------
✔ 간단한 경우       → \n 사용
✔ 여러 줄 입력      → """ """ (멀티라인 문자열) 사용
✔ 리스트 활용       → "\n".join(리스트) 사용
"""

# 리스트 활용 예시
lines = ["안녕하세요!", "파이썬 코딩 테스트", "화이팅!"]
message = "\n".join(lines)

print(message)

# 출력 결과:
# 안녕하세요!
# 파이썬 코딩 테스트
# 화이팅!


